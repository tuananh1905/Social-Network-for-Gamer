package modal;

/**
 *
 * @author TuanAnh
 */
public class Question {
    private int ID;
    private String content;

    public Question() {
    }

    public Question(int ID, String content) {
        this.ID = ID;
        this.content = content;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    
}
